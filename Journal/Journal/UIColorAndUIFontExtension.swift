import UIKit

extension UIFont {
    
    class func textStyle24Font() -> UIFont {
        return UIFont.systemFont(ofSize: 20.0, weight: UIFontWeightSemibold)
    }
    




}




extension UIColor {

    class var slate: UIColor {
        return UIColor(red: 67.0 / 255.0, green: 87.0 / 255.0, blue: 97.0 / 255.0, alpha: 1.0)
    }
}

